
public class Iteration3 
{
	public static void main(String args[])
	{
                for(char ch='A';ch<='F';ch++)            	
                {
                	for(char j='A';j<=ch;j++)        
		            {
                		System.out.print(j + " "); 
		            }
		            System.out.println();        
                }	
	}


} 
